$(SIGNATURES)
"""
function get_label(id::Int, meta::Array{Int64, 2} = METADATA)::Int64
	col = findfirst(x -> x == id, METADATA[1, :])
	if col == nothing
		return 0
	end
	return meta[2, col]
end

